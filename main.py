def get_max_number(): # определение функции
    # Алгоритм нахождения максимума
    numbers = [0, 1, -1, 5, 4, 2, 10, -100]
    max_number = 0
    for n in numbers:
        if n > max_number:
            max_number = n

    print(max_number)

